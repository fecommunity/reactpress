#!/usr/bin/env node
/**
 * Sync changelog H2 headings into locale-specific version nav for the blog sidebar.
 * Anchor IDs must match Docusaurus heading slugs in each locale's changelog.md.
 */
import fs from 'node:fs';
import path from 'node:path';
import {fileURLToPath} from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const docsRoot = path.resolve(__dirname, '..');

const SOURCES = [
  {
    input: path.join(docsRoot, 'blog/changelog.md'),
    output: path.join(docsRoot, 'src/changelog/versions.en.ts'),
    exportName: 'CHANGELOG_VERSIONS_EN',
  },
  {
    input: path.join(docsRoot, 'i18n/zh/docusaurus-plugin-content-blog/changelog.md'),
    output: path.join(docsRoot, 'src/changelog/versions.zh.ts'),
    exportName: 'CHANGELOG_VERSIONS_ZH',
  },
];

function parseHeading(line) {
  const match = line.match(/^## \[([^\]]+)\]\([^)]*\)\s*[（(](\d{4}-\d{2}-\d{2})[）)]/);
  if (!match) {
    return null;
  }
  const version = match[1];
  const date = match[2];
  const slug = version.replace(/\./g, '').replace(/-beta\.0/, '-beta0');
  return {
    id: `${slug}-${date}`,
    label: `${version} (${date})`,
  };
}

function writeVersions({input, output, exportName}) {
  const markdown = fs.readFileSync(input, 'utf8');
  const versions = markdown
    .split('\n')
    .filter((line) => line.startsWith('## ['))
    .map(parseHeading)
    .filter(Boolean);

  if (versions.length === 0) {
    throw new Error(`sync-changelog-nav: no version headings found in ${input}`);
  }

  const outputBody = `// Generated by scripts/sync-changelog-nav.mjs — do not edit manually.
export type ChangelogNavItem = {
  id: string;
  label: string;
};

export const ${exportName}: ChangelogNavItem[] = ${JSON.stringify(versions, null, 2)};
`;

  fs.mkdirSync(path.dirname(output), {recursive: true});
  fs.writeFileSync(output, outputBody);
  console.log(`sync-changelog-nav: wrote ${versions.length} versions to ${path.relative(docsRoot, output)}`);
}

for (const source of SOURCES) {
  writeVersions(source);
}

// Re-export helper for default import sites.
const indexOutput = path.join(docsRoot, 'src/changelog/versions.ts');
fs.writeFileSync(
  indexOutput,
  `// Generated by scripts/sync-changelog-nav.mjs — do not edit manually.
export type {ChangelogNavItem} from './versions.en';
export {CHANGELOG_VERSIONS_EN} from './versions.en';
export {CHANGELOG_VERSIONS_ZH} from './versions.zh';
`,
);
